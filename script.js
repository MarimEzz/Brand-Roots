function animation()
{
        const counters = document.querySelectorAll('.value');
        const speed = 350;
        
        counters.forEach( counter => {
           const animate = () => {
              const value = +counter.getAttribute('val');
              const data = +counter.innerText;
             
              const time = value / speed;
             if(data < value) {
                  counter.innerText = Math.ceil(data + time);
                  setTimeout(animate, 1);
                }else{
                  counter.innerText ='+' + value;
                }
             
           }
           
           animate();
        });
}

window.addEventListener('scroll' , ( ) =>{
    const homescroll = document.getElementById('home').getBoundingClientRect().top +  window.scrollY;
    const servicescroll = document.getElementById('service').getBoundingClientRect().top +  window.scrollY;
    const aboutscroll = document.getElementById('about').getBoundingClientRect().top +  window.scrollY;
    const contactscroll = document.getElementById('contact').getBoundingClientRect().top +  window.scrollY;
    const numanimation = document.getElementById('num-animate').getBoundingClientRect().top +  window.scrollY;
    const scrolled = window.scrollY ;
    //console.log(servicescroll)  ; 
    //console.log(scrolled)  ; 

    if ( Math.ceil ( scrolled ) >= homescroll ) 
    {
        home();
    }
    if ( Math.ceil ( scrolled ) >= servicescroll ) 
    {
        service();
    }
    if (Math.ceil ( scrolled ) >= aboutscroll)
    {
        about();
    }
    if (Math.ceil ( scrolled ) >= contactscroll)
    {
        contact();
    }
    if (Math.ceil ( scrolled ) >= numanimation)
    {
        animation();
    }
    
});